    public EditBook_page(Book book) {

    initComponents();
    setLocationRelativeTo(null); 

    
    bookID_tf.setText(String.valueOf(book.getBookId()));
    bookID_tf.setEditable(false); // ID can not be change by design

    title_tf.setText(book.getTitle());
    year_tf.setText(String.valueOf(book.getYear()));
    pages_tf.setText(String.valueOf(book.getNumberOfPages()));
    cover_tf.setText(book.getCover());
    about_tf.setText(book.getAbout());
    read_tf.setText(String.valueOf(book.getRead()));
    rating_tf.setText(String.valueOf(book.getRating()));
    comments_tf.setText(book.getComments());

    if (book.getReleaseDate() != null) {
        releasedate_tf.setText(book.getReleaseDate().toString());
    } else {
        releasedate_tf.setText("");
    }

    authorID_tf.setText(String.valueOf(book.getAuthorId()));
}


    public EditBook_page(int bookId) {
    initComponents();

    DataBase db = new DataBase();
    Book book = db.getBookById(bookId);

    if (book != null) {
        title_tf.setText(book.getTitle());
        year_tf.setText(String.valueOf(book.getYear()));
        pages_tf.setText(String.valueOf(book.getNumberOfPages()));
        cover_tf.setText(book.getCover());
        about_tf.setText(book.getAbout());
        read_tf.setText(String.valueOf(book.getRead()));
        rating_tf.setText(String.valueOf(book.getRating()));
        comments_tf.setText(book.getComments());
        releasedate_tf.setText(book.getReleaseDate() != null ? book.getReleaseDate().toString() : "");
        authorID_tf.setText(String.valueOf(book.getAuthorId()));
    } else {
        JOptionPane.showMessageDialog(this, "No book found with ID: " + bookId);
        this.dispose(); 
    }
}




