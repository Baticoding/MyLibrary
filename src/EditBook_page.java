    private void update_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_buttonActionPerformed
    
    try {
        
        int bookId = Integer.parseInt(bookID_tf.getText().trim());
        String title = title_tf.getText().trim();
        int year = Integer.parseInt(year_tf.getText().trim());
        int pages = Integer.parseInt(pages_tf.getText().trim());
        String cover = cover_tf.getText().trim();
        String about = about_tf.getText().trim();
        int read = Integer.parseInt(read_tf.getText().trim());
        int rating = Integer.parseInt(rating_tf.getText().trim());
        String comments = comments_tf.getText().trim();
        String releaseDate = releasedate_tf.getText().trim();
        int authorId = Integer.parseInt(authorID_tf.getText().trim());

        // database update
        DataBase db = new DataBase();
        boolean success = db.updateBook(bookId, title, year, pages, cover, about, read, rating, comments, releaseDate, authorId);

        if (success) {
            JOptionPane.showMessageDialog(this, "Book updated successfully!");
            this.dispose(); 
            new GUI().setVisible(true);// pencereyi kapat
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update book.");
        }

         } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }



    }//GEN-LAST:event_update_buttonActionPerformed

