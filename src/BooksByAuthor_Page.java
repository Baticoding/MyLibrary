    public BooksByAuthor_Page(List<Book> books, String authorFullName, int userType) {
        
        this.userType = userType;
        setTitle("Books by " + authorFullName);
        setSize(900, 400);
        setLocationRelativeTo(null);
        setLayout(new BorderLayout());

      
        String[] columns = {
            "Book ID", "Author ID", "Title", "Year", "Pages",
            "Cover", "About", "Read", "Rating", "Comments", "Release Date"
        };
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        for (Book b : books) {
            model.addRow(new Object[]{
                b.getBookId(),
                b.getAuthorId(),
                b.getTitle(),
                b.getYear(),
                b.getNumberOfPages(),
                b.getCover(),
                b.getAbout(),
                b.getRead(),
                b.getRating(),
                b.getComments(),
                b.getReleaseDate()
            });
        }

        JTable table = new JTable(model);
        JScrollPane scrollPane = new JScrollPane(table);
        add(scrollPane, BorderLayout.CENTER);

        //we can not add directly by palette so we add the button by code.
        JButton backButton = new JButton("Back to Main Menu");
        backButton.addActionListener(e -> {
      
                this.setVisible(false);
                return;

        });

        JPanel bottomPanel = new JPanel();
        bottomPanel.add(backButton);
        add(bottomPanel, BorderLayout.SOUTH);
    }



   
    

    
    
    

