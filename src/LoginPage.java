    private void login_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_buttonActionPerformed
        
                                                                                       
    String username = username_textfield.getText();
    String password = new String(password_textfield.getPassword());

    DataBase db = new DataBase();

    if (db.login(username, password)) {
        User u = db.getUser();
        JOptionPane.showMessageDialog(this, "Login successful! Welcome, " + u.getUsername());

        // üõéÔ∏è Notify for wish list books ‚úÖ 
        List<Book> upcoming = db.getUpcomingWishlistBooks(); 
        if (!upcoming.isEmpty()) {
            StringBuilder info = new StringBuilder("üì¢ Upcoming Wish List Books (next 7 days):\n\n");
            for (Book book : upcoming) {
                info.append("üìö Title: ").append(book.getTitle()).append("\n")
                    .append("üìÖ Release Date: ").append(book.getReleaseDate()).append("\n\n");
            }
            JOptionPane.showMessageDialog(this, info.toString(), "Upcoming Releases", JOptionPane.INFORMATION_MESSAGE);
        }

    
    int userType = u.getUserType(); // 1 = admin, 2 = normal user


    if (userType == 1) {
    new GUI().setVisible(true);
    } else {
    new UserFrame().setVisible(true);
    }
    dispose();

       
    } else {
        JOptionPane.showMessageDialog(this, "Login failed. Username or password is incorrect.");
    }


                
    }//GEN-LAST:event_login_buttonActionPerformed

